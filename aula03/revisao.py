# Revisão de operadores aritméticos


num1 = 13
num2 = 29

soma = num1 + num2
sub = num1 - num2
mult = num1 * num2
div = num2 / num1
div_int = num2 // num1    # divisão inteira. Essa divisão elimina os números depois da vírgula
resto = num2 % num1       # retorna o resto da divisão
potencia = num1 ** 2      # eleva o primeiro não ao segundo número


print(f'Soma: {soma}')
print(f'Subtração: {sub}')
print(f'Multiplicação: {mult}')
print(f'Divisão: {div}')
print(f'Divisãio inteira: {div_int}')
print(f'Resto: {resto}')
print(f'Potência: {potencia}')

