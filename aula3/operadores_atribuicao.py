# Os operadores de atribuição combinam uma operação aritmética
# com a atribuição do valor. Exemplo soma += 1

numero = 10
print(numero)

# adicionando 5 à váriavel numero sem usar o operador de atribuição
numero = numero + 5
print(f'Novo valor de número: {numero}.')

# Operador adição e artibuição
numero += 5      # numero = numero + 5
print(f'Novo valor de número: {numero}.')

# Operador subtração e atribuição
numero -= 8     # equivalente à numero = numero - 8
print(f'Valor de número menos 8: {numero}.')

# Operador multiplicação e atribuição
numero *= 10    # equivalente à numero = numero * 10
print(f"Valor de número multiplicado por 10: {numero}.")

# Operador divisão e atribuição
numero /= 2     # equivalente à numero = numero / 2
print(f'Valor de número dividido por 2: {numero}.')

